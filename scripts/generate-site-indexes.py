def hub_hero(title: str, lead: str, meta: str | None = None) -> list[str]:
    lines = [
        "---",
        f'title: "{title}"',
        "---",
        "",
        GENERATED,
        "",
        '<div class="odtis-hub-hero" markdown="1">',
        "",
        f"# {title}",
        "",
        lead,
        "",
    ]
    if meta:
        lines.extend([f'<p class="odtis-hub-meta" markdown="1">{meta}</p>', ""])
    lines.extend(["</div>", "", "---", ""])
    return lines

